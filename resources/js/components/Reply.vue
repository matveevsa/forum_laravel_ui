<script>
    export default {
        props: {
            attributes: Object
        },

        data() {
            return {
                body: this.attributes.body,
                editing: false
            }
        },

        methods: {
            update() {
                axios.patch('/replies/' + this.attributes.id, {
                    body: this.body
                });

                this.editing = false;

                flash('Updated!');
            }
        }
    }
</script>